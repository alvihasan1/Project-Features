let showImages = document.getElementById("story-head");
showImages.innerHTML = counter;

let fullStory = document.getElementById("full-story");

showImages.addEventListener("click", function (e) {
  fullStory.style.display = "block";
  fullStory.style.backgroundImage = `url(${arr[e.target.id].story})`;

  setTimeout(function () {
    fullStory.style.display = "none";
  }, 2000);
});
